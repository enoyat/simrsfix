    public void cari() {
       try {
            String sql = "SELECT * FROM pasien left join asuransi on pasien.kd_asuransi=asuransi.kd_asuransi where kd_pasien= '"+kd_pasien.getText()+"'";
            st = (Statement) conn.getKoneksi().createStatement();
            rs = st.executeQuery(sql);
            String [] data = new String[9];
            while(rs.next()) {
                kd_pasien.setText(rs.getString("kd_pasien")); 
                nama_pasien.setText(rs.getString("nama_pasien"));
                alamat_pasien.setText(rs.getString("alamat_pasien"));
                tempat.setText(rs.getString("tempat"));
                tgl_lahir.setDate(Date.valueOf(rs.getString("tgl_lahir")));
                jenis.setSelectedItem(rs.getString("jenis"));
                gol_darah.setSelectedItem(rs.getString("gol_darah")); 
                pekerjaan.setText(rs.getString("pekerjaan")); 
                namaorangtua.setText(rs.getString("namaorangtua")); 
                telpon.setText(rs.getString("telpon")); 
               
                agama.setSelectedItem(rs.getString("agama")); 
                if (rs.getString("jenis").equals("L")) {
                    jenis.setSelectedItem("Laki-Laki"); 
                }
                else {
                    jenis.setSelectedItem("Perempuan"); 
                }             
                status.setSelectedItem(rs.getString("status")); 
                nama_asuransi.setSelectedItem(rs.getString("nama_asuransi")); 
                noasuransi.setText(rs.getString("noasuransi")); 
            }
        }
        catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    public boolean cek(String kode) throws SQLException {
                
        String sqlq ="select * from pasien where kd_pasien= '"+kode+"'";
        st = (Statement) Koneksi.getKoneksi().createStatement();
        ResultSet rs3 = st.executeQuery(sqlq);
        while (rs3.next()) {
            return !kode.equals(rs3.getString("kd_pasien"));
        }
        return true;
    }
    
